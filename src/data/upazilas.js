// Get upazilas for a specific district
export const getUpazilasByDistrict = (districtKey, language = 'bn') => {
  const upazilas = upazilasByDistrict[districtKey];
  if (!upazilas) return [];
  return upazilas.map(upazila => upazila[language]);
};

// Search upazilas
export const searchUpazilas = (query, language = 'bn') => {
  const searchTerm = query.toLowerCase();
  const results = [];
  
  Object.entries(upazilasByDistrict).forEach(([district, upazilas]) => {
    upazilas.forEach(upazila => {
      if (upazila[language].toLowerCase().includes(searchTerm)) {
        results.push({
          district,
          upazila: upazila[language],
          fullName: `${upazila[language]}, ${district}`
        });
      }
    });
  });
  
  return results;
};

export default upazilasByDistrict;