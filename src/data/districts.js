// Flat array of all 64 districts for easy access
export const allDistricts = Object.values(bangladeshDistricts)
  .flatMap(division => division.districts);

// Get district names in specific language
export const getDistrictNames = (language = 'en') => {
  return allDistricts.map(district => district[language]);
};

// Get districts by division
export const getDistrictsByDivision = (divisionKey, language = 'en') => {
  const division = bangladeshDistricts[divisionKey];
  if (!division) return [];
  return division.districts.map(district => district[language]);
};

// Search districts by name
export const searchDistricts = (query, language = 'en') => {
  const searchTerm = query.toLowerCase();
  return allDistricts.filter(district => 
    district[language].toLowerCase().includes(searchTerm)
  );
};

// Get division name by district
export const getDivisionByDistrict = (districtName, language = 'en') => {
  for (const [divisionKey, division] of Object.entries(bangladeshDistricts)) {
    const found = division.districts.find(district => 
      district[language].toLowerCase() === districtName.toLowerCase()
    );
    if (found) {
      return division.name[language];
    }
  }
  return null;
};

export default bangladeshDistricts;